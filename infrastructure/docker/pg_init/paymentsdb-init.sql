-- PAYMENTS DB
DROP TABLE IF EXISTS payment_network;
CREATE TABLE payment_network (
    id VARCHAR(50) NOT NULL PRIMARY KEY,
    network_name VARCHAR(50),
    network_description VARCHAR(255),
    create_date timestamp,
    modify_date timestamp
);
